            <h3>What is Kneejerk Development Menu Swapper</h3>
            <p>
                This is a WordPress Plugin designed to allow you to contextualize the menus in your theme based on
                whether or not your visitor is logged in or not. It's especially handy when this functionality is
                not built into your theme!
            </p>

            <p>
                Please report any bugs or feel free to give me any feedback at
                <a href="https://kneejerk.dev/">https://kneejerk.dev/</a> via the contact form at the bottom.
            </p>

            <h3>How does it work</h3>
            <p>
                After installing KJD Menu Swapper, you'll see the Menu Swapper on the left admin navigation (at this point, you're
                probably already familiar with it). Once you click there, you'll be presented with the configuration
                for the plugin. This plugin does a few things to work:
            </p>
            <ol>
                <li>
                    KJD grabs the menus that your theme has registered. Whatever your theme, if it registers its menus
                    properly, KJD Menu Swapper will find it =]
                </li>
                <li>KJD then grabs the menus that you've configured under Appearance -> Menus</li>
                <li>
                    If a menu is configured for a registered theme menu, KJD Menu Swapper will show you. Otherwise,
                    it'll say "None Selected" like this:<br>
                    <img src="<?php echo KJD_MENU_SWAPPER_URI ?>/images/twenty_twenty_example.png" style="margin: .4em; outline:1px dashed red">
                </li>
                <li>
                    In the dropdown next to what the theme is configured for, you have the option to "swap" it when the
                    visitor is logged in:<br>
                    <img src="<?php echo KJD_MENU_SWAPPER_URI ?>/images/twenty_twenty_menu_select.png" style="margin: .4em; outline:1px dashed red">
                </li>
                <li>
                    Click the enable checkbox for that menu to enable the swap:<br>
                    <img src="<?php echo KJD_MENU_SWAPPER_URI ?>/images/twenty_twenty_configure_enable.png" style="margin: .4em; outline:1px dashed red">
                </li>
                <li>
                    Next, bonk the "Save" button, and the menu swap is live!<br>
                    <img src="<?php echo KJD_MENU_SWAPPER_URI ?>/images/twenty_twenty_save.png" style="margin: .4em; outline:1px dashed red">
                </li>
            </ol>
